create table PARAMS(

ATTR_ID BIGINT(20) NOT NULL,

OBJECT_ID BIGINT(20) NOT NULL,

ATTR_ACCESS_TYPE INT(20) NOT NULL,

VALUE VARCHAR(25) NOT NULL,

